package mano;

public class Switchcase {

	public static void main(String[] args) {
		int num=3;
		switch(num)
		{
			case 1: System.out.println("correct");
			case 2: System.out.println("incorrect");
			case 3: System.out.println("correct"); break;
			case 4: System.out.println("incorrect");
		}
	}

}
